const DICTS: Record<Lang, Dict> = { en, es, de, fr, it, pt, nl, zh, ja, ar };

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<Ctx | null>(null);

const STORAGE_KEY = "hl-lang";

function detectInitial(): Lang {
  if (typeof window === "undefined") return "en";
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
    if (stored && DICTS[stored]) return stored;
  } catch {}
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase() as Lang;
  return DICTS[nav] ? nav : "en";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    const initial = detectInitial();
    setLangState(initial);
    applyHtmlLang(initial);
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {}
    applyHtmlLang(l);
    setGoogleTransCookie(l);
    if (typeof window !== "undefined") {
      window.location.reload();
    }
  }, []);

  const t = useCallback(
    (key: string) => {
      const dict = DICTS[lang] || en;
      return dict[key] ?? en[key] ?? key;
    },
    [lang]
  );

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

function applyHtmlLang(l: Lang) {
  if (typeof document === "undefined") return;
  document.documentElement.setAttribute("lang", l);
  const meta = LANGS.find((x) => x.code === l);
  document.documentElement.setAttribute("dir", meta?.rtl ? "rtl" : "ltr");
}

function setGoogleTransCookie(l: Lang) {
  if (typeof document === "undefined") return;
  const value = l === "en" ? "" : `/en/${l}`;
  const host = location.hostname;
  const parts = host.split(".");
  const root = parts.length > 1 ? "." + parts.slice(-2).join(".") : host;
  const expire = "expires=Fri, 31 Dec 9999 23:59:59 GMT";
  document.cookie = `googtrans=${value}; path=/; ${expire}`;
  document.cookie = `googtrans=${value}; domain=${host}; path=/; ${expire}`;
  document.cookie = `googtrans=${value}; domain=${root}; path=/; ${expire}`;
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    return {
      lang: "en",
      setLang: () => {},
      t: (k: string) => en[k] ?? k,
    };
  }
  return ctx;
}
